    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        String fileName = f.getAbsolutePath();
        
        File nf = new File(fileName);
        BufferedReader br = null;
        try {
            br = new BufferedReader(new FileReader(nf));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String inputs,result="";
        try {
            while((inputs = br.readLine())!=null) {
                 
                    result = result.concat(inputs+"\n");
//                    System.out.println(inputs);
                
            }
        } catch (IOException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.jTextArea1.setText(result);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        StringReader sr = new StringReader(jTextArea1.getText());
        BufferedReader nbr = new BufferedReader(sr);
        String nextline="",output="";
        String dom = jTextField1.getText();
        String regex = "[a-zA-Z0-9._-]+@"+dom+"\\.com";
        try {
            while ((nextline = nbr.readLine())!=null){
                if(nextline.matches(regex)) {
                    output = output.concat(nextline+"\n");			
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.jTextArea2.setText(output);
    }//GEN-LAST:event_jButton2ActionPerformed

