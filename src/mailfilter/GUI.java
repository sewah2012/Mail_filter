    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser nchooser = new JFileChooser("C:\\");
        nchooser.setDialogTitle("Save your Email List");
       // nchooser.setFileFilter(new FileFilter (".txt","Only Text format"));
        int result = nchooser.showSaveDialog(null); 
        if(result == JFileChooser.APPROVE_OPTION){
            String content = this.jTextArea2.getText();
            File fi = nchooser.getSelectedFile();
            try{
                FileWriter fw = new FileWriter(fi.getPath());
                fw.write(content);
                fw.flush();
                fw.close();
            }catch(Exception ee){
                JOptionPane.showMessageDialog(null,ee.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

